# write a program to check user input abbreviation
print("Enter the abbreviation\n")
abr = input()
if abr == "lol":
    print("laughing out loud")
elif abr == "rofl":
    print("rolling on floor laughing")
elif abr == "lmk":
    print("Let me know")
elif abr == "smh":
    print("shaking my head")
else:
    print("Not valid abbreviation")

